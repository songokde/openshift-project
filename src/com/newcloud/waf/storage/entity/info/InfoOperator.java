package com.newcloud.waf.storage.entity.info;

import com.newcloud.waf.storage.entity.BaseEntity;

public class InfoOperator extends BaseEntity {
	private static final long serialVersionUID = -3718760790636718374L;
	
	private Integer opId;
	private Integer opCustId;
	private String opName;
	private String opPwd;
	private String opLinkman;
	private String opTelphone;
	private String opEmail;
	private Short opType;

	public InfoOperator() {
	}

	public InfoOperator(Integer opId) {
		this.opId = opId;
	}

	public InfoOperator(Integer opId, Integer opCustId, String opName, String opPwd, String opLinkman,
			String opTelphone, String opEmail, Short opType) {
		super();
		this.opId = opId;
		this.opCustId = opCustId;
		this.opName = opName;
		this.opPwd = opPwd;
		this.opLinkman = opLinkman;
		this.opTelphone = opTelphone;
		this.opEmail = opEmail;
		this.opType = opType;
	}

	public Integer getOpId() {
		return opId;
	}

	public void setOpId(Integer opId) {
		this.opId = opId;
	}

	public Integer getOpCustId() {
		return opCustId;
	}

	public void setOpCustId(Integer opCustId) {
		this.opCustId = opCustId;
	}

	public String getOpName() {
		return opName;
	}

	public void setOpName(String opName) {
		this.opName = opName;
	}

	public String getOpPwd() {
		return opPwd;
	}

	public void setOpPwd(String opPwd) {
		this.opPwd = opPwd;
	}

	public String getOpLinkman() {
		return opLinkman;
	}

	public void setOpLinkman(String opLinkman) {
		this.opLinkman = opLinkman;
	}

	public String getOpTelphone() {
		return opTelphone;
	}

	public void setOpTelphone(String opTelphone) {
		this.opTelphone = opTelphone;
	}

	public String getOpEmail() {
		return opEmail;
	}

	public void setOpEmail(String opEmail) {
		this.opEmail = opEmail;
	}

	public Short getOpType() {
		return opType;
	}

	public void setOpType(Short opType) {
		this.opType = opType;
	}

}